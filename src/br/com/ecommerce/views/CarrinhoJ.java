    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        if(tablelivros.getSelectedRow() != -1);{
            try {
            Livro liv = new Livro();
            LivroDAO livdao = new LivroDAO();
            
            liv.setLiv_titulo(txtlivro.getText());
            liv.setLiv_preco(Double.parseDouble(txtpreco.getText()));
            liv.setLiv_id((int) tablelivros.getValueAt(tablelivros.getSelectedRow(), 0));
                livdao.delete(liv);
                txtlivro.setText("");
                txtpreco.setText("");
                        
                new CarrinhoJ().setVisible(true);
                
            } catch (SQLException ex) {
                Logger.getLogger(CarrinhoJ.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

